    private void tableUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableUserMouseClicked
      try{
        
            int fila = tableUser.getSelectedRow();
            txtIdUserFrm.setText(tableUser.getValueAt(fila, 0).toString());
            txtCedula.setText(tableUser.getValueAt(fila, 1).toString());
            txtContrasenaUser.setText(tableUser.getValueAt(fila, 2).toString());
            txtNombreCompleto.setText(tableUser.getValueAt(fila, 3).toString());
            cboxTipoUserFrm.setSelectedItem(tableUser.getValueAt(fila, 4));
            
            
        }catch (Exception e){
        
            JOptionPane.showMessageDialog(this, "ERROR AL SELECCIONAR UN REGISTRO:" + e.getMessage());
            
        }       
    }//GEN-LAST:event_tableUserMouseClicked

